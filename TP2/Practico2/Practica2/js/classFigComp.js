class FigCompuesta{
    constructor(fig1,fig2){
        this.fig1 = fig1;
        this.fig2 = fig2;
    }

    getFig1(){
        return this.fig1;
    }
    getFig2(){
        return this.fig2;
    }

    setFig1(fig){
        this.fig1 = fig;
    }
    setFig2(fig){
        this.fig2 = fig;
    }
}